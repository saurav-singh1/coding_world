    <h1>Java Script</h1>
    <p class="js">JavaScript is one of the most popular computer languages. This programming language is essential for any web
        developer. It’s mostly used to enhance HTML pages and is commonly found embedded in HTML code. JavaScript as a
        language is an interpreted one, and as such, it doesn’t need to be compiled. Through JavaScript, many games and
        communication applications run directly in the browser build.</p>
    <h2>Benifits & Drawbacks</h2>
    <p class="js">When we talk about the benefits of JavaScript, there are a few that we can include:</p>
    <h3>Speed</h3>
    <p class="js">JavaScript is agile because it can be run instantly within the client-side browser. JavaScript is unhindered by
        network calls to a backend server</p>
    <h3>Popularity</h3>
    <p class="js">JavaScript is one of the most used coding languages on the web.</p>
    <h3>Simplicity</h3>
    <p class="js">It is quite uncomplicated to learn and implement.</p>
    <h3>Interoperability</h3>
    <p class="js">JavaScript collaborates well with other languages and is used in a wide variety of applications.
        As for the disadvantages of JavaScript, they’re not that much in number, but they are a few that we could
        mention.</p>
    <h3>Client-side security</h3>
    <p class="js">The code of JavaScript is visible on the computers of users, and in some instances, it may be exploited for
        malicious purposes.
    <p>
    <h3>Browser Support</h3>
    <p class="js">JavaScript is interpreted differently in different browsers. That’s why before it’s published, it needs to be run
        on various platforms.</p>
