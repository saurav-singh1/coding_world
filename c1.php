        <h1>C</h1>
        <p class="c">C language can be a good foundation for all those that want to learn how to program. This language
            was among the first programming languages ever developed, and it still continues to be considered as the
            foundation for many modern coding languages. C runs on any type of device is often used to program embedded
            devices in automobiles and medical devices.</p>
        <h2>Benifits & Drawbacks</h2>
        <p class="c">C, as a programming language, has several advantages when used. Let’s see what those advantages
            are.</p>
        <h3>Built-in functions</h3>
        <p class="c">In ANSI C, the C programming language has only 32 keywords that have built-in functions. These
            functions can be quite helpful in program building with C.</p>
        <h3>The foundation of other programming languages</h3>
        <p class="c">If you’re just starting with programming, then you definitely will start with C, as it’s considered
            to be the fundamental coding language. A number of other programming languages are built with the base of
            the C language.
        <p>
        <h3>Quality to extend itself</h3>
        <p class="c">C has the ability to extend itself. This ability comes as a result of the C language having its own
            set of functions in the C library. You can also add your own functions to the C Standard Library, and the
            code becomes simpler.</p>
        <h3>Dynamic memory-allocation</h3>
        <p class="c">Thanks to the dynamic memory allocation, you’re free to allocate memory at run time. This means
            that even if you don’t know how much memory is required by objects in your program, you can still run it in
            C and assign the memory simultaneously.

            Just like other coding languages, C too has a couple of disadvantages.</p>
        <h3>OOps concept</h3>
        <p class="c">Even though C is a broad programming language, it doesn’t support the OOPs (Inheritance,
            Polymorphism, Encapsulation, Abstraction, Data Hiding) concept.
        <p>
        <h3>Concept of namespace</h3>
        <p class="c">A namespace allows the programmer to reuse the names in different contexts. C doesn’t implement the
            concept of THE namespace, and, as a result, one cannot use a variable with the same name.</p>

